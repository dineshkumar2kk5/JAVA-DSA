public class ArraySum {

    public static void main(String[] args) {
        int sum =0;

        int [] array={1,2,3,5,6,4,3,56,4};
        for(int i=0;
            i<array.length;
            i=i+1

        ){

            sum=sum+array[i];
            System.out.println(sum);
            

        }

        System.out.println(sum);
    }

    
}
